def human_years_cat_years_dog_years(human_years):
    if human_years == 1:
        return [1, 15, 15]  # პირველ წელს კატის და ძაღლის ასაკი 15-ია
    elif human_years == 2:
        return [2, 24, 24]  # მეორე წელს კატის და ძაღლის ასაკი 24-ია
    else:
        # მესამე და მეტი წლისთვის
        cat_years = 24 + (human_years - 2) * 4
        dog_years = 24 + (human_years - 2) * 5
        return [human_years, cat_years, dog_years]
    


