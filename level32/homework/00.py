def change_sign(num):
    return -num

# Test cases
print(change_sign(7))   # Output: -7
print(change_sign(-11))  # Output: 11
print(change_sign(0))   # Output: 0
print(change_sign(2))    # Output:-2
print(change_sign(5))   # Output: -5
print(change_sign(-7))  # Output: 7
print(change_sign(0))   # Output: 0
print(change_sign(99))    # Output:-99
